function drawproduct() {
    let Title100Day = document.querySelector(".sec-title");
    Title100Day.innerHTML = `${AllDays.length} Day With Projects` ;

    let productUi = AllDays.map((item) => {
        if (item.id) {
            return `
            <div class="services-card">
            ${item.new ? `<span class="new">${item.new}</span>` : "" }
                <div class="services-img">
                    <img loading="lazy" alt="${item.heading}" src="${item.imgUrl}" loading="lazy" alt="">
                </div>
                <div class="services-info">
                    <h3 class="title">${item.heading}</h3>
                    <p>${item.desc}</p>
                    <a herf="#" onClick="saveItem(${item.id})">Read More</a>
                </div>
            </div>
            `;    
        } else {
            return `
            <div class="services-card">
                <span class="new">Soon</span>
                <span class="soon"><img src="../assest/coming-soon.png"/></span>
                
                
                <div class="services-img">
                    <img loading="lazy" alt="${item.heading}" src="${item.imgUrl}" loading="lazy" alt="">
                </div>
                <div class="services-info">
                    <h3 class="title">${item.heading}</h3>
                    <p>${item.desc}</p>
                    <a herf="#">Coming Soon</a>
                </div>
            </div>
            `;    
        }
    });
    servicescards.innerHTML = productUi.join(''); 

    localStorage.setItem("allProdcut" , JSON.stringify(AllDays));
}
drawproduct();

function saveItem(id) {
    localStorage.setItem("productId" , id)
    window.location = "infoProduct.html"
}

function searchCards() {
    var input, filter, cards, card, title, i;
    input = document.getElementById("searchInput");
    filter = input.value.toUpperCase();
    cards = document.getElementById("cards");
    card = cards.getElementsByClassName("services-card");

    for (i = 0; i < card.length; i++) {
        title = card[i].getElementsByClassName("title")[0];
        if (title.innerHTML.toUpperCase().indexOf(filter) > -1) {
            card[i].style.display = "";
        } else {
            card[i].style.display = "none";
        }
    }
}